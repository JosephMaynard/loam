// Generate a release signing keystore for the LOAM Android host, one command.
//
//   pnpm --filter app keystore
//
// Creates `apps/app/release.jks` (a 2048-bit RSA key, 10000-day validity) and writes
// `apps/app/keystore.properties` with strong random passwords. Both are gitignored (`*.jks` and the
// keystore.properties rule), so nothing secret is committed. Once they exist, `pnpm --filter app apk`
// signs the release APK with this key (via plugins/with-release-signing.js) instead of the throwaway
// debug key. Existing files are never overwritten — delete them by hand to rotate.
//
// KEEP A BACKUP of release.jks and keystore.properties somewhere safe: Android requires every update
// to an installed app to be signed with the SAME key, so losing it means users must uninstall before
// they can take an update. (For Play Store distribution, prefer Play App Signing — this local key
// then becomes the upload key.)

import { spawnSync } from "node:child_process";
import { randomBytes } from "node:crypto";
import { existsSync, writeFileSync } from "node:fs";
import { homedir, platform } from "node:os";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const appDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const keystorePath = join(appDir, "release.jks");
const propsPath = join(appDir, "keystore.properties");
const alias = "loam";

if (existsSync(keystorePath) || existsSync(propsPath)) {
  console.error(
    `A keystore already exists:\n  ${existsSync(keystorePath) ? keystorePath : propsPath}\n` +
      "Refusing to overwrite it — delete both files by hand to generate a fresh key (see the note in this script).",
  );
  process.exit(1);
}

/** Locate `keytool`: prefer JAVA_HOME, then Android Studio's bundled JBR on macOS, else the PATH. */
function resolveKeytool() {
  const candidates = [];
  if (process.env.JAVA_HOME) {
    candidates.push(join(process.env.JAVA_HOME, "bin", "keytool"));
  }
  if (platform() === "darwin") {
    candidates.push("/Applications/Android Studio.app/Contents/jbr/Contents/Home/bin/keytool");
  }
  candidates.push(join(homedir(), ".sdkman", "candidates", "java", "current", "bin", "keytool"));
  for (const candidate of candidates) {
    if (existsSync(candidate)) {
      return candidate;
    }
  }
  return "keytool"; // fall back to PATH
}

const storePassword = randomBytes(24).toString("base64url");
const keyPassword = randomBytes(24).toString("base64url");
const keytool = resolveKeytool();

// Pass the passwords via `:env` (read from the child's environment) rather than as CLI args, so they
// never appear in a `ps`/process listing. The `:env` modifier is a long-standing keytool feature.
const result = spawnSync(
  keytool,
  [
    "-genkeypair",
    "-v",
    "-keystore", keystorePath,
    "-alias", alias,
    "-keyalg", "RSA",
    "-keysize", "2048",
    "-validity", "10000",
    "-storepass:env", "LOAM_STOREPASS",
    "-keypass:env", "LOAM_KEYPASS",
    "-dname", "CN=LOAM Host, OU=LOAM, O=LOAM, C=US",
  ],
  {
    stdio: ["ignore", "inherit", "inherit"],
    env: { ...process.env, LOAM_STOREPASS: storePassword, LOAM_KEYPASS: keyPassword },
  },
);

if (result.error) {
  console.error(
    `\nCould not run keytool (${keytool}): ${result.error.message}\n` +
      "keytool ships with the JDK — set JAVA_HOME to a real JDK (Android Studio's JBR works) and retry.",
  );
  process.exit(1);
}
if (result.status !== 0) {
  console.error(`\nkeytool failed (exit ${result.status}).`);
  process.exit(result.status ?? 1);
}

// storeFile is written relative to apps/app so the checkout is portable; the plugin resolves it.
writeFileSync(
  propsPath,
  [
    "# LOAM Android release signing — generated by scripts/generate-keystore.mjs. Gitignored; keep a backup.",
    "storeFile=release.jks",
    `storePassword=${storePassword}`,
    `keyAlias=${alias}`,
    `keyPassword=${keyPassword}`,
    "",
  ].join("\n"),
  { mode: 0o600 },
);

console.log(`\n[32m✓ Release keystore created:[0m ${keystorePath}`);
console.log(`  Credentials written to: ${propsPath} (gitignored — back it up!)`);
console.log("  Build a signed release APK with:  pnpm --filter app apk");
